  // Balanced Growth Palette - Core Colors
  static const Color primaryLight = Color(0xFF2D5A4A); // Deep forest green
  static const Color primaryVariantLight =
      Color(0xFF1E3D32); // Darker forest green
  static const Color secondaryLight = Color(0xFFF4F1E8); // Warm off-white
  static const Color secondaryVariantLight =
      Color(0xFFE8E3D8); // Slightly darker off-white
  static const Color backgroundLight =
      Color(0xFFF4F1E8); // Warm off-white background
  static const Color surfaceLight = Color(0xFFFFFFFF); // Clean surface
  static const Color errorLight = Color(0xFFE74C3C); // Clear error red
  static const Color successLight = Color(0xFF27AE60); // Success green
  static const Color warningLight = Color(0xFFF39C12); // Warning orange
  static const Color accentLight = Color(0xFFE67E22); // Energizing orange

  // Text colors - optimized for mobile readability
  static const Color textPrimaryLight = Color(0xFF2C3E50); // High contrast dark
  static const Color textSecondaryLight = Color(0xFF7F8C8D); // Supporting text
  static const Color dividerLight = Color(0xFFECF0F1); // Subtle separation

  // On colors for light theme
  static const Color onPrimaryLight = Color(0xFFFFFFFF);
  static const Color onSecondaryLight = Color(0xFF2C3E50);
  static const Color onBackgroundLight = Color(0xFF2C3E50);
  static const Color onSurfaceLight = Color(0xFF2C3E50);
  static const Color onErrorLight = Color(0xFFFFFFFF);

  // Dark theme colors - maintaining wellness aesthetic
  static const Color primaryDark = Color(0xFF4A8B7A); // Lighter forest green
  static const Color primaryVariantDark =
      Color(0xFF2D5A4A); // Original forest green
  static const Color secondaryDark = Color(0xFF2C3E50); // Dark surface
  static const Color secondaryVariantDark =
      Color(0xFF34495E); // Lighter dark surface
  static const Color backgroundDark = Color(0xFF1A1A1A); // Deep background
  static const Color surfaceDark = Color(0xFF2C3E50); // Dark surface
  static const Color errorDark = Color(0xFFE74C3C); // Same error red
  static const Color successDark = Color(0xFF27AE60); // Same success green
  static const Color warningDark = Color(0xFFF39C12); // Same warning orange
  static const Color accentDark = Color(0xFFE67E22); // Same energizing orange

  // Text colors for dark theme
  static const Color textPrimaryDark = Color(0xFFF4F1E8); // Light text
  static const Color textSecondaryDark =
      Color(0xFF95A5A6); // Secondary light text
  static const Color dividerDark = Color(0xFF34495E); // Dark divider

  // On colors for dark theme
  static const Color onPrimaryDark = Color(0xFFFFFFFF);
  static const Color onSecondaryDark = Color(0xFFF4F1E8);
  static const Color onBackgroundDark = Color(0xFFF4F1E8);
  static const Color onSurfaceDark = Color(0xFFF4F1E8);
  static const Color onErrorDark = Color(0xFFFFFFFF);

